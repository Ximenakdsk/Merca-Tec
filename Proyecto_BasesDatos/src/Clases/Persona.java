package Clases;

/**
 *
 * @author Juan Pablo
 */
public class Persona {
    String id;
    String nombre;
    String apellidoP;
    String apellidoM;
    String telefono;
    String email;
    String contraseña;
    String coDomicilio;
    String codigoPostal;

    public Persona(String id, String nombre, String apellidoP, String apellidoM, String telefono, String email, String contraseña, String codigoPostal, String coDomicilio ) {
        this.id = id;
        this.nombre = nombre;
        this.apellidoP = apellidoP;
        this.apellidoM = apellidoM;
        this.telefono = telefono;
        this.email = email;
        this.contraseña = contraseña;
        this.codigoPostal = codigoPostal;
        this.coDomicilio = coDomicilio;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getApellidoP() {
        return apellidoP;
    }

    public void setApellidoP(String apellidoP) {
        this.apellidoP = apellidoP;
    }

    public String getApellidoM() {
        return apellidoM;
    }

    public void setApellidoM(String apellidoM) {
        this.apellidoM = apellidoM;
    }

    public String getTelefono() {
        return telefono;
    }

    public void setTelefono(String telefono) {
        this.telefono = telefono;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getContraseña() {
        return contraseña;
    }

    public void setContraseña(String contraseña) {
        this.contraseña = contraseña;
    }

    public String getCoDomicilio() {
        return coDomicilio;
    }

    public void setCoDomicilio(String coDomicilio) {
        this.coDomicilio = coDomicilio;
    }

    public String getCodigoPostal() {
        return codigoPostal;
    }

    public void setCodigoPostal(String codigoPostal) {
        this.codigoPostal = codigoPostal;
    }
    
    
}
