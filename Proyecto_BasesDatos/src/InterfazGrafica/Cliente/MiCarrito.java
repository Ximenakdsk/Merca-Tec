    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        ProductosService PS = new ProductosService();
        ImagenService IS = new ImagenService();
        Productos producto;
        String nombre = "";
        String descripcion = "";
        double precio = 0;
        int calif = 0;
        int row = tblCarrito.getSelectedRow();
        if (row != -1) {
            nombre = (String)modelo.getValueAt(row, 0);
            descripcion = (String)modelo.getValueAt(row, 1);
            precio = (Double)modelo.getValueAt(row, 2);
            try {
                producto = PS.readProductoPorNombre(nombre);
                System.out.println("Producto: " + producto.getNombre() + "ID: " + producto.getIdProd());
                cliente.getCarrito().eliminarProducto(producto);
                for (Productos p : cliente.getCarrito().verProductos()) {
                    System.out.println(p.getNombre());
                }
                DefaultTableModel modelo = (DefaultTableModel) tblCarrito.getModel(); 
                modelo.removeRow(row);
                tblCarrito.revalidate();
                tblCarrito.repaint();
                lblTotal.setText("Total: " + cliente.getCarrito().calcularTotal());
                lblTotal.revalidate();
                lblTotal.repaint();
            } catch (SQLException ex) {
                Logger.getLogger(MostrarProductos.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            JOptionPane.showMessageDialog(null, "NO SE SELECCIONO PRODUCTO", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

