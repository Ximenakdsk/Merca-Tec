    public void ListarCarrito(){
        ClientesService CS = new ClientesService();
        ProductosService PS = new ProductosService();
        ArrayList<Productos> carrito = cliente.getCarrito().verProductos();
        modelo = (DefaultTableModel)tblCarrito.getModel();
        Object ob[] = new Object[4];
        for (Productos p : carrito) {
            ob[0] = p.getNombre();
            ob[1] = p.getDescripcion();
            ob[2] = p.getPrecio();
            ob[3] = p.getNoCateg();
            modelo.addRow(ob);
        }
        tblCarrito.setModel(modelo);
    }

