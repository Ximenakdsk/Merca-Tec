    public String mostrarCiudad(String codigo) throws SQLException{
        String ciudad = "";
        DomicilioService DS = new DomicilioService();
        Domicilio dom = DS.readDomicilioByCodigo(codigo);
        ciudad = dom.getCiudad() + ", " + dom.getEstado() + ", " + dom.getPais();
        return ciudad;
    }
    
    public void listarMetodosPago(Clientes cliente) throws SQLException{
        String idCte = cliente.getId();
        MetodosDePagoService MPS = new MetodosDePagoService();
        List<MetodoDePago> MetodosPago = MPS.readAllMp(idCte);
        for (MetodoDePago m : MetodosPago) {
            cbMetodosPago.addItem(m.getNumTarjeta());
        }
    }
    
    public void ListarCarrito(){
        ClientesService CS = new ClientesService();
        ProductosService PS = new ProductosService();
        ArrayList<Productos> productos = cliente.getCarrito().verProductos();
        modelo = (DefaultTableModel)tblProductos.getModel();
        Object ob[] = new Object[3];
        for (Productos p : productos) {
            ob[0] = p.getNombre();
            ob[1] = p.getDescripcion();
            ob[2] = p.getPrecio();
            modelo.addRow(ob);
        }
        tblProductos.setModel(modelo);
    }

