    private void btnRealizarPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRealizarPedidoActionPerformed
        // TODO add your handling code here:
        ComprasService CS = new ComprasService();
        DetVentasService DVS = new DetVentasService();
        EnvioService ES = new EnvioService();
        ArrayList<String> idsCompras = new ArrayList<>();
        ArrayList<String> idsEnvios = new ArrayList<>();
        ArrayList<Productos> listaProd = cliente.getCarrito().verProductos();
        try {
            idsCompras = CS.getAllComprasIDs();
            idsEnvios = ES.getAllEnviosIDs();
        } catch (SQLException ex) {
            Logger.getLogger(RealizarCompra.class.getName()).log(Level.SEVERE, null, ex);
        }
        DetVentas detVenta;
        Compra compra;
        Envio envio;
        Proveedores prov;
        String idProv;
        String idProd = "";
        int i = 0;
        int iEnv= 0;
        String idEnvio = "";
        String idCompra = "";
        String numProds = "";
        double total = 0;
        if (!cbMetodosPago.getSelectedItem().equals("---")) {
            try{
                LocalDate fechaActual = LocalDate.now();
                LocalDate fechaTresDiasDespues = fechaActual.plusDays(3);
                DateTimeFormatter formatoFecha = DateTimeFormatter.ofPattern("dd/MM/yy");
                String fecha = fechaActual.format(formatoFecha);
                String fechaEntrega = fechaTresDiasDespues.format(formatoFecha);
                i = idsCompras.size() + 1;
                iEnv = idsEnvios.size() + 1;
                idCompra = "C" + i;
                idEnvio = "E"+iEnv;
                numProds = String.valueOf(cliente.getCarrito().calcularCantProd());
                total = cliente.getCarrito().calcularTotal();
                compra = new Compra(idCompra, cliente.getId(), fecha, numProds, total);
                CS.createAndUpdateCompra(compra);
                for (Productos p : listaProd) {
                    idProd = p.getIdProd();
                    detVenta = new DetVentas(idCompra, idProd);
                    DVS.createAndUpdateDetVentas(detVenta);
                }
                cliente.getCompras().add(compra);
                idProv = listaProd.get(0).getIdProd();
                envio = new Envio(idEnvio, idProv, idCompra, fecha, fechaEntrega);
                ES.createAndUpadateEnvio(envio);
                JOptionPane.showMessageDialog(this, "Pedido realizado con éxito.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
                cliente.getCarrito().eliminarTodosProductos();
                this.setVisible(false);
            }catch(SQLException ex) {
                    Logger.getLogger(RealizarCompra.class.getName()).log(Level.SEVERE, "Error al realizar el pedido", ex); 
                    JOptionPane.showMessageDialog(this, "Error al procesar el pedido. Intente de nuevo más tarde.", "Error", JOptionPane.ERROR_MESSAGE);
                }
        }else{
            JOptionPane.showMessageDialog(null, "ELIJA UN METODO DE PAGO", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnRealizarPedidoActionPerformed

