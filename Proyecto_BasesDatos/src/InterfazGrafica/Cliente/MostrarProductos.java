    private void btnMostrarDetallesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarDetallesActionPerformed
        // TODO add your handling code here:
        ProductosService PS = new ProductosService();
        ImagenService IS = new ImagenService();
        Productos producto;
        String nombre = "";
        String descripcion = "";
        double precio = 0;
        ImageIcon imF = null;
        int calif = 0;
        int row = tblProductos.getSelectedRow();
        if (row != -1) {
            nombre = (String)modelo.getValueAt(row, 0);
            descripcion = (String)modelo.getValueAt(row, 1);
            precio = (Double)modelo.getValueAt(row, 2);
            try {
                producto = PS.readProductoPorNombre(nombre);
                System.out.println("Producto: " + producto.getNombre() + "ID: " + producto.getIdProd());
                imF = IS.cargarImagen(producto.getIdProd());
                DetallesProducto DP = new DetallesProducto(this, true, producto, imF);
                DP.setVisible(true);
            } catch (SQLException ex) {
                Logger.getLogger(MostrarProductos.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(MostrarProductos.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnMostrarDetallesActionPerformed

    private void btnAgregarAlCarritoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarAlCarritoActionPerformed
        // TODO add your handling code here:
        Clientes cliente = this.cliente;
        int i = Integer.parseInt(txtCantidadP.getText());
        ProductosService PS = new ProductosService();
        ImagenService IS = new ImagenService();
        Productos producto;
        String nombre = "";
        String descripcion = "";
        double precio = 0;
        int row = tblProductos.getSelectedRow();
        if (row != -1) {
            nombre = (String)modelo.getValueAt(row, 0);
            descripcion = (String)modelo.getValueAt(row, 1);
            precio = (Double)modelo.getValueAt(row, 2);
            try {
                producto = PS.readProductoPorNombre(nombre);
                System.out.println("Producto: " + producto.getNombre() + "ID: " + producto.getIdProd());
                if (i != 0) {
                    for (int j = 0; j < i; j++) {
                        cliente.getCarrito().agregarProducto(producto);
                    }
                }else{
                    JOptionPane.showMessageDialog(null, "CANTIDAD DE PRODUCTOS INVALIDA", "ERROR", JOptionPane.ERROR_MESSAGE);
                }
            } catch (SQLException ex) {
                Logger.getLogger(MostrarProductos.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnAgregarAlCarritoActionPerformed

