    public void ListarProductos(){
        ProductosService PS = new ProductosService();
        ArrayList<Clases.Productos> productos = PS.readAllProductosCateg(categoria.getNoCateg());
        modelo = (DefaultTableModel)tblProductos.getModel();
        Object ob[] = new Object[4];
        for (Clases.Productos p : productos) {
            ob[0] = p.getNombre();
            ob[1] = p.getDescripcion();
            ob[2] = p.getPrecio();
            ob[3] = p.getCalif();
            modelo.addRow(ob);
        }
        tblProductos.setModel(modelo);
    }
