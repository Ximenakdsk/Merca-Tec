    public void listarPedidos(){
        ArrayList<Compra> pedidos = cliente.getCompras();
        if (!pedidos.isEmpty()) {
            modelo = (DefaultTableModel)tblPedidos.getModel();
        Object ob[] = new Object[4];
        for (Compra c : pedidos) {
            ob[0] = c.getIdCompra();
            ob[1] = c.getFecha();
            ob[2] = c.getNumProd();
            String total = "$" + String.valueOf(c.getTotal());
            ob[3] = total;
            modelo.addRow(ob);
        }
        tblPedidos.setModel(modelo);
        }else{
            JOptionPane.showMessageDialog(this, "No tienes pedidos", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

