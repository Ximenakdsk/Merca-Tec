    private void btnDetallesCompraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetallesCompraActionPerformed
        // TODO add your handling code here:
        ComprasService CS = new ComprasService();
        String idCompra = "";
        int row = tblPedidos.getSelectedRow();
        if (row != -1) {
            idCompra = (String)modelo.getValueAt(row, 0);
            try {
                Compra compra = CS.readCompraPorId(idCompra);
                if (compra != null) {
                    System.out.println("Compra: " + compra.getIdCompra());
                    DetallesCompra DC = new DetallesCompra(cliente, compra);
                    DC.setVisible(true);
                }else{
                    JOptionPane.showMessageDialog(this, "No se encontró la compra con ID: " + idCompra, "Error", JOptionPane.ERROR_MESSAGE);
                }
                
            } catch (SQLException ex) {
                Logger.getLogger(MostrarProductos.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            JOptionPane.showMessageDialog(this, "No se seleccionó ninguna compra", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDetallesCompraActionPerformed

