    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String nombre = "";
        String apellidoP = "";
        String apellidoM = "";
        String telefono = "";
        String email = "";
        String contrase単a = "";
        String codigoPostal = "";
        String coDomicilio = "";
        String rfc = "";
        int i = 0;
        String id = "";
        ProveedoresService ser = new ProveedoresService();
        ArrayList<String> listaP = null;
        try {
            listaP = ser.getAllProveedoresIDs();
        } catch (SQLException ex) {
            Logger.getLogger(RegistroProv.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        if (txtNom.getText().equals("") || txtApp.getText().equals("") || txtApm.getText().equals("") || txtTel.getText().equals("") || txtEmail.getText().equals("")|| txtContra.getText().equals("") || txtCP.getText().equals("") || cbCiudades.getSelectedItem().equals("---") || txtRFC.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "HAY ESPACIOS VACIOS", "ERROR", JOptionPane.ERROR_MESSAGE);
        }else{
            nombre = txtNom.getText();
            apellidoP = txtApp.getText();
            apellidoM = txtApm.getText();
            telefono = txtTel.getText();
            email = txtEmail.getText();
            contrase単a = txtContra.getText();
            codigoPostal = txtCP.getText();
            coDomicilio = String.valueOf(cbCiudades.getSelectedItem());
            rfc = txtRFC.getText();
            i = listaP.size() + 1;
            id = "P" + i;
            Proveedores prov = new Proveedores(id, nombre, apellidoP, apellidoM, telefono, email, contrase単a, codigoPostal, coDomicilio, rfc);
            try {
                ser.createAndUpdateProveedor(prov);
                listaP.add(id);
                JOptionPane.showMessageDialog(null, "SU ID DE PROVEEDOR ES: " + id, "INFO", JOptionPane.INFORMATION_MESSAGE);
            } catch (SQLException ex) {
                Logger.getLogger(RegistroProv.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

