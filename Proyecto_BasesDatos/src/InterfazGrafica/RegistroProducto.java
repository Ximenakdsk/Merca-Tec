    private void btnInsertarImgActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarImgActionPerformed
        // TODO add your handling code here:
        JFileChooser fc = new JFileChooser();
        fc.setFileSelectionMode(JFileChooser.FILES_ONLY);
        //Configurar filtro
        FileNameExtensionFilter filtro = new FileNameExtensionFilter("Archivos de imagen", "jpg", "jpeg", "png", "gif");
        fc.setFileFilter(filtro);
        
        int resultado = fc.showOpenDialog(null);
        if (resultado == JFileChooser.APPROVE_OPTION) {
            selectedFile = fc.getSelectedFile();
            if (selectedFile != null) {
                txtRuta.setText(selectedFile.getAbsolutePath() + " ");
                ImageIcon imIc = new ImageIcon(selectedFile.getAbsolutePath());
                lblImagen.setIcon(imIc);
            }
        }
    }//GEN-LAST:event_btnInsertarImgActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String idProd = "";
        String noCateg = "";
        String idProv = "";
        String nombre = "";
        String descripcion = "";
        double precio = 0;
        int calificacion = 0;
        int i = 0;
        int im = 0;
        String idImagen = "";
        ProductosService PS= new ProductosService();
        ImagenService IS = new ImagenService();
        ArrayList<String> listaP= null;
        ArrayList<String> listaI = null;
        try{
            listaP = PS.getAllProductosIDs();
            listaI = IS.getAllImagenIDs();
        } catch (SQLException ex){
            Logger.getLogger(RegistroProducto.class.getName()).log(Level.SEVERE, null, ex);
        }    
        
        if (txtCateg.getText().equals("") || txtProvID.getText().equals("") || txtNomProd.getText().equals("") || txtDesc.getText().equals("") || txtPrecio.getText().equals("")|| txtCalif.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "HAY ESPACIOS VACIOS", "ERROR", JOptionPane.ERROR_MESSAGE);
        }else{
            noCateg = txtCateg.getText();
            idProv = txtProvID.getText();
            nombre = txtNomProd.getText();
            descripcion = txtDesc.getText();
            precio = Double.parseDouble(txtPrecio.getText());
            calificacion = Integer.parseInt(txtCalif.getText());
            i = listaP.size() +1;
            im = listaI.size()+ 1;
            idProd = "P" + i;
            idImagen = "I" + im;
            Imagen img = null;
            Productos prod = new Productos(idProd, noCateg, idProv, nombre, descripcion, precio, calificacion);
            if (selectedFile != null) {
                ImageReader reader = new ImageReader();
                try {
                    byte[] imagenBytes = reader.leerImagen(selectedFile);
                    img = new Imagen(idImagen, idProd, imagenBytes);
                } catch (IOException ex) {
                    Logger.getLogger(RegistroProducto.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            
            try {
                PS.createAndUpdateProducto(prod);
                IS.createAndUpdateImagen(img, listaProductos, prod.getIdProd());
                listaP.add(idProd);
                listaI.add(idImagen);
                JOptionPane.showMessageDialog(null, "EL ID DEL PRODUCTO ES: " + idProd, "INFO", JOptionPane.INFORMATION_MESSAGE);
            } catch (SQLException ex) {
                Logger.getLogger(RegistroProv.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

